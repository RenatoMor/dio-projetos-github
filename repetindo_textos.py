# Solicitar uma string e um número ao usuário e repetir a string a quantidade de vezes do número informado.

# Recebe a string e o número do usuário
texto = input("Digite uma string: ")
repetir = int(input("Digite um número: "))

# Repete a string a quantidade de vezes do número informado
resultado = texto * repetir

# Imprime o resultado
print("A repetição da string é:", resultado)
